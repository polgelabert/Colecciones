public class Objeto {

    public String nombreobj;
    public String tipo;
    public String descripcion;
    public int valor;
    public int coste;

    public Objeto (String nobj, String t, String des, int v, int c){
        this.nombreobj = nobj;
        this.tipo = t;
        this.descripcion = des;
        this.valor = v;
        this.coste = c;
    }

}
